namespace Computools_Task;

/// <summary>
/// Represents the different grant levels a student can receive based on their academic performance.
/// </summary>
public enum Grant
{
    /// <summary>
    /// No grant is awarded to the student.
    /// </summary>
    None,

    /// <summary>
    /// A regular grant is awarded to the student.
    /// </summary>
    Regular,

    /// <summary>
    /// An increased grant is awarded to the student.
    /// </summary>
    Increased,
}